# Convert C to F and F to C

# create function to convert celsius to fahrenheit and return the conversion
def cTof(c):
    return (9/5) * (c + 32)

# create function to convert fahrenheit to celsius and return the conversion
def fToc(f):
    return (5/9) * (f - 32)

